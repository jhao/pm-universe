def update_markdown(segment: str, slug: str, spec: dict[str, object]) -> None:
    doc_path = BASE_DIR / "docs" / segment / next(p.name for p in (BASE_DIR / "docs" / segment).glob(f"*-{slug}.md"))
    content = build_markdown(spec)
    doc_path.write_text(content, encoding="utf-8")


def update_prototype(segment: str, slug: str, spec: dict[str, object]) -> None:
    proto_dir = BASE_DIR / "prototypes" / segment / slug
    proto_dir.mkdir(parents=True, exist_ok=True)
    html_path = proto_dir / "index.html"
    html_content = build_html(spec)
    html_path.write_text(html_content, encoding="utf-8")


def build_mermaid(spec: dict[str, object]) -> str:
    lines: list[str] = [
        "%% Auto-generated by scripts/generate_assets.py",
        "mindmap",
        f"  root(({spec['title']} 功能结构))",
        f"    目标用户({spec['target_users']})",
        f"    核心痛点({spec['pain_points']})",
        f"    价值主张({spec['value']})",
        "    核心场景",
    ]
    for scenario in spec["scenarios"]:
        lines.append(f"      {scenario}")
    lines.append("    功能模块")
    for module in spec["modules"]:
        lines.append(f"      {module['name']}({module['goal']})")
        for feature in module["features"]:
            lines.append(f"        {feature}")
    lines.append("    关键指标")
    for metric in spec["metrics"]:
        lines.append(f"      {metric}")
    return "\n".join(lines) + "\n"


def update_diagram(segment: str, slug: str, spec: dict[str, object]) -> None:
    diagram_dir = BASE_DIR / "slides" / "diagrams" / segment
    diagram_dir.mkdir(parents=True, exist_ok=True)
    diagram_path = diagram_dir / f"{slug}.mmd"
    diagram_path.write_text(build_mermaid(spec), encoding="utf-8")




def main() -> None:
    for segment, products in PRODUCTS.items():
        for slug, spec in products.items():
            update_markdown(segment, slug, spec)
            update_prototype(segment, slug, spec)
            update_diagram(segment, slug, spec)


if __name__ == "__main__":
    main()
