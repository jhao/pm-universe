def build_markdown(spec: dict[str, object]) -> str:
    lines = [f"# {spec['title']}", "", f"## 整体需求概述", ""]
    lines.append(f"- **目标用户：** {spec['target_users']}")
    lines.append(f"- **核心痛点：** {spec['pain_points']}")
    lines.append(f"- **价值主张：** {spec['value']}")
    lines.append("")
    lines.append("## 核心使用场景")
    lines.append("")
    for idx, scenario in enumerate(spec["scenarios"], start=1):
        lines.append(f"{idx}. {scenario}")
    lines.append("")
    lines.append("## 功能模块设计")
    lines.append("")
    for module in spec["modules"]:
        lines.append(f"### {module['name']}")
        lines.append("")
        lines.append(f"- 功能目标：{module['goal']}")
        lines.append("- 关键特性：")
        for feature in module["features"]:
            lines.append(f"  - {feature}")
        lines.append("")
    lines.append("## 关键指标")
    lines.append("")
    for metric in spec["metrics"]:
        lines.append(f"- {metric}")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("> 本文档基于根目录 README.md 的项目目标整理，概述原型设计所需的整体需求。")
    return "\n".join(lines)


